function seedetail1(){
    var id = getUrlParam1();
    function getUrlParam1() {
        var reg = document.getElementById("goodsid1");
        return reg.innerHTML;
    }

    var user_id = getusergo_id(window.location.href);
    function getusergo_id(name){
        var usergo_id1 = name.match(/usergo_id=(\S*)/)[1];
        if(usergo_id1.length==21){
            usergo_id1=usergo_id1.substr(0,20);
        }
        return usergo_id1;
    }
    var aaa = "product-details.html?goodsid=" + id +"&usergo_id=" + user_id +"";
    window.open(aaa); 
}
